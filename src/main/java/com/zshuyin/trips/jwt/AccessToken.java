package com.zshuyin.trips.jwt;

import com.zshuyin.trips.bean.User;

public class AccessToken {
    private String token;
    private User user;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}